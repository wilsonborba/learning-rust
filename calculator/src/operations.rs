


#[allow(dead_code)]
pub fn add(a: f64, b: f64) -> f64 {
    a + b
}

#[allow(dead_code)]
pub fn subtract(a: f64, b: f64) -> f64 {
    a - b
}


#[allow(dead_code)]
pub fn multiply(a: f64, b: f64) -> f64 {
    a * b
}

#[allow(dead_code)]
pub fn divide(a: f64, b: f64) -> f64 {
    a / b
}

#[allow(dead_code)]
pub fn modulo(a: f64, b: f64) -> f64 {
    a % b
}

#[allow(dead_code)]
pub fn power(a: f64, b: f64) -> f64 {
    a.powf(b)
}




